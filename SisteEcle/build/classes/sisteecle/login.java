    public void validar() {
        int resultado = 0;
        String pass;
        pass = String.valueOf(contraseña.getPassword());
        String usu = usuario.getText();

        String SQL = " SELECT * FROM usuario WHERE usuario='" + usu + "' AND contra='" + pass + "'";

        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(SQL);
            if (rs.next()) {
                resultado = 1;

                if (resultado == 1) {
                    JOptionPane.showMessageDialog(null, "Usuario Registrado, Acceso Permitido");
                    menu mn = new menu();
                    mn.setVisible(true);
             
                }
            } else {
                JOptionPane.showMessageDialog(null, "Usuario no Regsitrado, Acceso Denegado");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error de Conexiòn");
        }
    }
